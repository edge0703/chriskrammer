<h1>Books</h1>

<p>This is a (by far not complete) list of books I totally adore and which helped me greatly to evolve as a web-designer.</p>

<ul>
	<li>
	<img src="img/books/transcendingcss.jpg" alt="">
	<h2><a href="http://www.transcendingcss.com/">Transcending CSS</a>, <i>Andy Clarke</i></h2>
	<p>One of my first books about web design and still by far my favorite one. It&rsquo;s a one-stop guide to everything related to the topic and still relevant after six years. It totally changed the understanding of my craft. </p>
	</li>
	
	<li>
	<img src="img/books/designingfortheweb.jpg" alt="">
	<h2><a href="http://designingfortheweb.co.uk/">Designing for the Web</a>, <i>Mark Boulton</i></h2>
	<p>Not only that Mark Boulton is my favorite designer &ndash; a true hero for me &ndash;, he also writes fanatastic and relevant books. If you need one book about web-design in general, then it&rsquo;s this one. You will find almost no code in it, but a lot of basic information you need as a professional. There is also a <a href="http://designingfortheweb.co.uk/book/index.php">free online version</a>, but I strongly advise you to buy the book.</p>
	</li>

	<li>
	<img src="img/books/designisajob.jpg" alt="">
	<h2><a href="http://www.abookapart.com/products/design-is-a-job">Design is a job</a>, <i>Mike Monteiro</i></h2>
	<p>This is the latest addition to my library but already one of the books, I couldn&rsquo;t live without. Since I changed my job recently I got very much out of this book. Not only how to deal with clients but also how to handle your colleagues. Especially if you plan to start as a freelancer this is the one book for you.</p>
	</li>

	<li>
	<img src="img/books/vignellicanon.jpg" alt="">
	<h2><a href="http://www.google.at/url?sa=t&rct=j&q=&esrc=s&source=web&cd=3&cad=rja&ved=0CDgQFjAC&url=http%3A%2F%2Fwww.amazon.com%2FThe-Vignelli-Canon-Massimo%2Fdp%2F3037782250&ei=aoeNUPKNH4jOtAbktYGAAw&usg=AFQjCNG6h5jttvihDWmysdCXG1rlz10X4Q">The Vignelli Canon</a>, <i>Massimo Vignelli</i></h2>
	<p>My bible. Everything in this book is so true, so inspriational, so straight to the point. The author &ndash; Massimo Vignelli, a New York based designer born in Milano, Italy &ndash; writes about the basic topics of design and how he and his design studio employ them. In every statement you feel the passion of this man about design, which simply infects you to emulate his deeds. Although the book is available as a <a href="hhttp://www.vignelli.com/canon.pdf">free PDF</a> you should definitely buy a printed copy.</p>
	</li>
	
	<li>
	<img src="img/books/theprinciplesof.jpg" alt="">
	<h2><a href="http://www.sitepoint.com/books/design2/">The Principles of Beautiful Web Design</a>, <i>Jason Beird</i></h2>
	<p>Another book about web-design techniques which tought me very much I know today. It not only gives you an understanding of the technical side but also holds information about the very basics &ndash; design principles, color, etc.</p>
	</li>
	
	<li>
	<img src="img/books/responsivewebdesign.jpg" alt="">
	<h2><a href="http://www.abookapart.com/products/responsive-web-design">Responsive Web Design</a>, <i>Ethan Marcotte</i></h2>
	<p>Maybe the most important contemporary piece of writing, which probably changed a whole generation. Ethan Marcotte told us that the web is no fixed place anymore and that it is essential to be as flexible and fluid as possible with a website.</p>
	</li>
	
	<li>
	<img src="img/books/hardboiled.jpg" alt="">
	<h2><a href="http://hardboiledwebdesign.com/">Hardboiled Web Design</a>, <i>Andy Clarke</i></h2>
	<p>Another gem by Andy Clarke, which mainly told me how important it is to leave Photoshop as a web-design tool behind and directly jump into the browser as soon as the general page structure is set. </p>
	</li>
	
	<li>
	<img src="img/books/thelementsof.jpg" alt="">
	<h2><a href="http://www.amazon.com/Elements-Typographic-Style-Robert-Bringhurst/dp/0881792063/ref=sr_1_1?s=books&ie=UTF8&qid=1349959802&sr=1-1&keywords=The+Elements+of+Typographic+Style">The Elements of Typographic Style</a>, <i>Robert Bringhurst</i></h2>
	<p>The bible. Do I have to say anymore? Since <a href="http://informationarchitects.net/blog/the-web-is-all-about-typography-period/">web design is 95% typography</a> you also need a book which teaches you how to deal with this masses of texts. It&rsquo;s voluminous and you will need some time to digest all of its content, but this effort is totally of worth.</p>
	</li>
	
	<li>
	<img src="img/books/universaldesign.jpg" alt="">
	<h2><a href="http://www.amazon.com/Universal-Principles-Design-Revised-Updated/dp/1592535879/ref=sr_1_1?s=books&ie=UTF8&qid=1349959831&sr=1-1&keywords=Universal+Principles+of+Design">Universal Principles of Design</a>, <i>William Lidwell, Kritina Holden, Jill Butler</i></h2>
	<p>Since the web doesn&rsquo;t consist of type alone you should also know how to deal with the remaining 5%. The authors decribe a total of 125 principles on two sides each with supporting examples. Follow them and you will design the best websites of your life.</p>
	</li>	

	<li>
	<img src="img/books/thinkingwithtype.jpg" alt="">
	<h2><a href="http://www.amazon.com/Thinking-Type-2nd-revised-expanded/dp/1568989695/ref=dp_ob_title_bk">Thinking with type</a>, <i>Ellen Lupton</i></h2>
	<p>If you need one short and precise book about typography, get this one. Ellen Lupton&rsquo;s publication teaches you everything you need to know about the history of typefaces, describes all the important terms and not least how to actually employ fonts and what you need to consider. A true gem, which I will often refer to in the future.</p>
	</li>	
</ul>

